@lru_cache(maxsize=1)
def iq1_grid_i8() -> np.ndarray:
    """Return the IQ1 codebook as an int8 array of shape (2048, 8)."""
    raw = base64.b64decode(_IQ1_GRID_B64)
    grid = np.frombuffer(raw, dtype=np.int8)
    if grid.size != IQ1_GRID_SHAPE[0] * IQ1_GRID_SHAPE[1]:
        raise ValueError(f"IQ1 grid has unexpected size {grid.size}")
    return grid.reshape(IQ1_GRID_SHAPE).copy()
